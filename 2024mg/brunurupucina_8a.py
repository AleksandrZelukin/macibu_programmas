from turtle import *
import tkinter as tk
t = Turtle()

krasa = ("red","yellow","blue","lightblue")
speed(1000)
def saule():
    pu()
    goto(-300,200)
    pd()
    fillcolor(krasa[1])
    pencolor(krasa[1])
    begin_fill()
    for i in range(36):
        fd(200)
        rt(180)
        rt(10)
    end_fill()
poga1 = tk.Button(text="SAULE",command=saule)
poga1.place(x=260,y=180)
poga2 = tk.Button(text="SIENA")
poga2.place(x=40,y=70) 
poga3 = tk.Button(text="DURVIS")
poga3.place(x=40,y=100)
poga4 = tk.Button(text="JUMTA")
poga4.place(x=40,y=130)
poga5 = tk.Button(text="LOGS")
poga5.place(x=40,y=160)  
pencolor(krasa[0])
def figura(x,y,z,n,m):
    pu()
    goto(x,y)
    pd()
    fillcolor(krasa[m])
    begin_fill()
    for i in range(n):
        fd(z)
        lt(360/n)
    end_fill()
    
def truba(x,y,z,m):
    pu()
    goto(x,y)
    pd()
    fillcolor(krasa[m])
    begin_fill()
    fd(z-40)
    lt(90)
    fd(z)
    lt(90)
    fd(z-40)
    lt(90)
    fd(z)
    lt(90)
    end_fill()
        
siena = figura(-75,-380,150,4,0)
durvis = truba(-60,-380,80,3)
logs = figura(10,-300,40,4,3)
caurule = truba(20,-122,60,0)
jumta = figura(-100,-230,200,3,1)

mainloop()
