from turtle import *
krasa = ("red","yellow","blue","lightblue")
speed(1000)
pu()
goto(-300,200)
pd()
fillcolor(krasa[1])
pencolor(krasa[1])
begin_fill()
for i in range(36):
    fd(200)
    rt(180)
    rt(10)
end_fill()
pencolor(krasa[0])
def figura(x,y,z,n,m):
    pu()
    goto(x,y)
    pd()
    fillcolor(krasa[m])
    begin_fill()
    for i in range(n):
        fd(z)
        lt(360/n)
    end_fill()
    
def truba(x,y,z,m):
    pu()
    goto(x,y)
    pd()
    fillcolor(krasa[m])
    begin_fill()
    fd(z-40)
    lt(90)
    fd(z)
    lt(90)
    fd(z-40)
    lt(90)
    fd(z)
    lt(90)
    end_fill()
        
siena = figura(-75,-380,150,4,0)
durvis = truba(-60,-380,80,3)
logs = figura(10,-300,40,4,3)
caurule = truba(20,-122,60,0)
jumta = figura(-100,-230,200,3,1)

mainloop()
