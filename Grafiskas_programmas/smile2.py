from turtle import *
def smile (x=0,y=0):
    pensize(4)
    ht()
    speed(100)
    pencolor("darkred")
    fillcolor("yellow")
    up()
    setpos(0+x,-100+y)
    begin_fill()
    down()
    circle(100) #Seja
    end_fill()
    up()
    setpos(-40+x,30+y)
    down()
    dot(40)
    up()
    setpos(40+x,30+y)
    down()
    dot(40)
    up()
    setpos(-70+x,-30+y)
    setheading(-60)
    pensize(8)
    down()
    circle(80,120)
    up()
    home()
smile(0,0)
smile(-300,250)
smile(-300,-250)
smile(300,250)
smile(300,-250)

exitonclick()

