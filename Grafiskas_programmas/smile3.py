from turtle import*

def smile (x=0,y=0):
    ht()
    up()
    pensize(1)
    setpos(0+x,-100+y)
    down()
    circle (100)
    up()
    setpos(0+x,0+y)
    down()
    dot(5)
    up()
    setpos(-70+x,-30+y)
    setheading(-60)
    pensize(8)
    down()
    circle(80,120)
    up()
    home()
smile(-200,300)
smile(200,300)
smile(200,-300)
