from turtle import *
a = Turtle()
a.screen.setup(800, 800)
a.up()
a.pensize(4)
a.goto(-200, 200)
a.down()
a.fillcolor("blue")
a.begin_fill()
a.goto(200, 200)
a.goto(200, - 200)
a.goto(-200,-200)
a.goto(-200, 200)
a.end_fill()
a.up()
a.pensize(2)
a.goto(-150,150)
a.down()
a.fillcolor("red")
a.begin_fill()
a.goto(150,150)
a.goto(150,-150)
a.goto(-150,-150)
a.goto(-150,150)
a.end_fill()
a.up()
pensize(5)
a.goto(-100,100)
a.down()
a.fillcolor("yellow")
a.begin_fill()
a.goto(100,100)
a.goto(100,-100)
a.goto(-100,-100)
a.goto(-100,100)
a.end_fill()
a.screen.exitonclick()
a.screen.mainloop()
