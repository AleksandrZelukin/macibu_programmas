import turtle as t
t.shape("turtle")

t.up()
t.goto(-100,-150)
t.down()
t.fillcolor("lightblue")
t.begin_fill()
for i in range (4):
    t.fd(200)
    t.rt(90)
t.end_fill()
t.up()
t.goto(20,-100)
t.down()
t.fillcolor("brown")
t.begin_fill()
t.fd(30)
t.lt(90)
t.fd(200)
t.lt(90)
t.fd(30)
t.lt(90)
t.fd(200)
t.end_fill()
t.up()
t.home()
t.goto(-100,-150)
t.down()
t.fillcolor("lightblue")
t.begin_fill()
for i in range (3):
    t.fd(200)
    t.lt(120)
t.end_fill()



t.up()
t.goto(-50,-250)
t.down()
t.color("sandy brown") #https://trinket.io/docs/colors
t.fillcolor("sandy brown")
t.begin_fill()

for i in range(4):
    t.fd(100)
    t.rt(90)
t.fd(100)
t.lt(90)
t.circle(50,180)
t.end_fill()
t.up()
t.goto(-100,-150)
t.down()
t.pencolor("blue")
t.write("Su≈Üa buda",font=("Arial",32, "bold"))
